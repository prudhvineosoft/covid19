class NewState extends Component {
  state = {homeData: '', box: false}

  componentDidMount() {
    this.gettingData()
  }

  gettingData = async () => {
    const response = await fetch(
      'https://data.covid19india.org/v4/min/data.min.json',
    )
    const data = await response.json()
    this.setState({homeData: data, box: true})
  }

  render() {
    const {homeData, box} = this.state
    const {match} = this.props
    const {params} = match
    const {id} = params
    const state = id.substring(1).toUpperCase()
    const filtered = statesList.filter(each2 => each2.state_code === state)
    const searchInput = filtered[0].state_name

    return (
      <div className="about-bg-container">
        {box ? (
          <EachState
            stateData={homeData[state]}
            searchInput={searchInput}
            stateCode={state}
          />
        ) : (
          <div testid="loader" className="loader">
            <Loader type="TailSpin" color="#00BFFF" height={50} width={50} />
          </div>
        )}
      </div>
    )
  }
}

export default NewState
